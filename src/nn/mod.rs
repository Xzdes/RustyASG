//! Модуль, содержащий строительные блоки для нейронных сетей.
//!
//! В новой графовой архитектуре, каждый "слой" является конструктором,
//! который добавляет определенный паттерн узлов (операций и параметров)
//! в Абстрактный Семантический Граф (ASG).
//!
//! # Основные компоненты
//!
//! - [`Module`](module::Module): Основной трейт, который должны реализовывать все слои.
//! - [`Linear`](linear::Linear): Полносвязный слой.

// Объявляем файлы как публичные субмодули внутри `nn`.
// Начнем с linear, остальные добавим по мере их переписывания.
pub mod linear;
pub mod module;


// Ре-экспортируем самые важные структуры для удобства.
