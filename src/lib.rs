//! # RustyGradients 2.0 (Ядро)
//!
//! `rusty-gradients` - это компилируемый фреймворк для глубокого обучения
//! на Rust, построенный вокруг концепции Абстрактного Семантического Графа (ASG).
//!
//! Вместо немедленного выполнения операций (eager execution), фреймворк
//! сначала строит граф вычислений. Этот граф затем может быть оптимизирован
//! и скомпилирован для выполнения на различных бэкендах, таких как CPU и GPU.
//!
//! # Основные компоненты ядра:
//!
//! - `asg`: Ядро, определяющее структуру графа (`Asg`, `Node`, `NodeType`).
//! - `tensor`: Символьный дескриптор (`Tensor`) для построения графа и
//!   контекст графа (`GraphContext`).
//! - `autograd`: Механизм автоматического дифференцирования (Graph-to-Graph).
//! - `nn`: Высокоуровневые строительные блоки (слои), которые добавляют
//!   паттерны узлов в граф.
//! - `runtime`: Исполнительные среды (бэкенды), которые могут выполнять
//!   построенный ASG. На данный момент реализован простой `Interpreter`.
//! - `losses`: Функции потерь для обучения моделей.
//! - `optimizers`: Алгоритмы оптимизации для обновления весов.

